package com.sb;

public class javaoperators {

	public static void main(String args[]) {
		int num =15;
		int num1 = 45;
		
		System.out.println(num+num1);

		System.out.println(num-num1);

		System.out.println(num*num1);

		System.out.println(num/num1);

		System.out.println(num%num1);

	}
}
