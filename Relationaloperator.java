package com.op;

public class Relationaloperator {

	public static void main(String args[]) {
	int a = 21;
	int b = 41;
	boolean v = a>b;
	
	System.out.println(a>b);
	 System.out.println(a<b);
	 System.out.println(a>=b);
	 System.out.println(a<=b);
	 System.out.println(a==b);
	 System.out.println(a!=b);
	 System.out.println(v);
	 
	 
	}
}
